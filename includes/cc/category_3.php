        <h4 class="fw-bold">3. Issuance of Community Tax Certificate</h4>
        <p>A Community Tax Certificate (CTC) is required when an individual or corporation:</p>
        <ul class="list-group list-group-flush">
            <li class="list-group-item">Acknowledges any document before a notary public.</li>
            <li class="list-group-item">Receives any license certificate or permit from any public Authority.</li>
            <li class="list-group-item">Pays any tax or fee.</li>
            <li class="list-group-item">Receives money from any public fund.</li>
            <li class="list-group-item">Transacts other official business.</li>
            <li class="list-group-item">Receives any salary or wage from any person or corporation.</li>
        </ul>

