        <h4 class="fw-bold">Assessment and Collection of Business Permits and Licenses Thru CLGU System</h4>
        <p>Prior to legally operating a business, entrepreneurs should secure business license and mayor's permit. The office of the Municipal Treasurer assesses and commercial operations or venture. The license must be renewed from January 02 to January 20 every year. Penalties are imposed after this period. Business Taxes for new entreprises are based in capitalization. Those for succceeding years are computed based on percentage of gross receipt/sales or graduated fixed tax. Payments may be annually, semi-annually or quarterly. Taxes are due on the 20 days of earch quarter.</p>

